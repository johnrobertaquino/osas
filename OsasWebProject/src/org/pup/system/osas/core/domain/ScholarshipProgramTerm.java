package org.pup.system.osas.core.domain;

public class ScholarshipProgramTerm {
	
	private int scholarshipProgramTermId;
	
	private int scholarshipTermId;
	
	private int scholarshipProgramId;

	/**
	 * @return the scholarshipProgramTermId
	 */
	public int getScholarshipProgramTermId() {
		return scholarshipProgramTermId;
	}

	/**
	 * @param scholarshipProgramTermId the scholarshipProgramTermId to set
	 */
	public void setScholarshipProgramTermId(int scholarshipProgramTermId) {
		this.scholarshipProgramTermId = scholarshipProgramTermId;
	}

	/**
	 * @return the scholarshipTermId
	 */
	public int getScholarshipTermId() {
		return scholarshipTermId;
	}

	/**
	 * @param scholarshipTermId the scholarshipTermId to set
	 */
	public void setScholarshipTermId(int scholarshipTermId) {
		this.scholarshipTermId = scholarshipTermId;
	}

	/**
	 * @return the scholarshipProgramId
	 */
	public int getScholarshipProgramId() {
		return scholarshipProgramId;
	}

	/**
	 * @param scholarshipProgramId the scholarshipProgramId to set
	 */
	public void setScholarshipProgramId(int scholarshipProgramId) {
		this.scholarshipProgramId = scholarshipProgramId;
	}
}
