package org.pup.system.osas.core.domain;

public class UserRoleReference {
	
	private String userRoleReferenceCode;
	
	private String userRoleName;
	
	private String verbiage;
	
	public String getUserRoleReferenceCode() {
		return userRoleReferenceCode;
	}
	public void setUserRoleReferenceCode(String userRoleReferenceCode) {
		this.userRoleReferenceCode = userRoleReferenceCode;
	}
	public String getUserRoleName() {
		return userRoleName;
	}
	public void setUserRoleName(String userRoleName) {
		this.userRoleName = userRoleName;
	}
	public String getVerbiage() {
		return verbiage;
	}
	public void setVerbiage(String verbiage) {
		this.verbiage = verbiage;
	}
	
}
