package org.pup.system.osas.core.domain;

public class FirstTimeLoginReference {

	private String firstTimeLoginCode;
	
	private String description;

	public String getFirstTimeLoginCode() {
		return firstTimeLoginCode;
	}

	public void setFirstTimeLoginCode(String firstTimeLoginCode) {
		this.firstTimeLoginCode = firstTimeLoginCode;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
